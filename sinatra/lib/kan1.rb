class Kannada
 def self.convert(input)
 input = input.to_s
  if input == "Monday"
    "somavara"
  elsif input == "Tuesday"
    "mangalavara"
  elsif input == "Wednesday"
    "budhavara"
  elsif input == "Thursday"
    "guruvara"
  elsif input == "Fridayday"
    "shukravara"
  elsif input == "Saturday"
    "shanivara"
  else input == "Sunday"
    "ravivara"
 end
end
end

 
 
 
 
